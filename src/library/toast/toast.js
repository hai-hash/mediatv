import { toast } from 'react-toastify';
import 'react-toastify/dist/ReactToastify.css';
export const notifySuccess = (content) => toast.success(content,
    {
        position: "bottom-right",
        autoClose: 5000,
        hideProgressBar: false,
        closeOnClick: true,
        pauseOnHover: true,
        draggable: true,
        progress: undefined,
    });
